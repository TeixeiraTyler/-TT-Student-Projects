CREATE PROCEDURE gtr()
LANGUAGE SQL
BEGIN
SELECT P_CODE, INV_NUMBER, LINE_UNITS FROM line sold
WHERE sold.LINE_UNITS > (SELECT AVG(LINE_UNITS) FROM line average WHERE average.P_CODE = sold.P_CODE);

SELECT P_CODE, INV_NUMBER, LINE_UNITS, (SELECT AVG(LINE_UNITS) FROM line avsold WHERE avsold.P_CODE = sold.P_CODE) AS AVG_SOLD 
FROM line sold 
WHERE sold.LINE_UNITS > (SELECT AVG(LINE_UNITS) FROM line average Where average.P_CODE = sold.P_CODE);
END //

