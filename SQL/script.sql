DELIMITER //
CREATE PROCEDURE flv()
LANGUAGE SQL
BEGIN
	DECLARE pmax DECIMAL (8,2);

	(SELECT MAX(product.P_PRICE)
		FROM 
			product INNER JOIN vendor ON product.V_CODE = vendor.V_CODE
		WHERE vendor.V_STATE = "FL"
        INTO pmax);
        
    SELECT product.P_CODE, product.P_PRICE
	FROM product
	WHERE product.P_PRICE > pmax;
END //

